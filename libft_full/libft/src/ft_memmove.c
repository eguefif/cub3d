#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	void	*copy;

	copy = (void *) malloc(n);
	memcpy(copy, src, n);
	memcpy(dest, copy, n);
	free(copy);
	return (0);
}
