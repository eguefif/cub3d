#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	char	*b;

	b = (char *) big;
	while (*b != '\0')
	{
		if (*b == *little)
			if (ft_strncmp(b, little, len - 1) == 0)
				return (b);
		b++;
	}
	return (NULL);
}
