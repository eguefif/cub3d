#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	size_t	size;
	char	*pt;
	size_t	c;

	size = strlen(s);
	pt = ft_strnew(size + 1);
	c = 0;
	while (c < size)
	{
		pt[c] = f(s[c]);
		c++;
	}
	return (pt);
}
