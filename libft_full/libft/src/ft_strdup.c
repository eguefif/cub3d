#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	size;
	char	*str;

	size = ft_strlen(s);
	str = ft_strnew(size + 1);
	if (str != NULL)
		ft_strncpy(str, s, size + 1);
	return (str);
}
