#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*pt;

	pt = (t_list *) malloc(sizeof(t_list));
	pt->content = malloc(content_size);
	ft_memcpy(pt->content, content, content_size);
	pt->content_size = content_size;
	pt->next = NULL;
	return (pt);
}
