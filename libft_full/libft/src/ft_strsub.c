#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*pt;

	pt = (char *) malloc(len + 1);
	if (pt == NULL)
		return (pt);
	ft_strncpy(pt, &s[start], len);
	pt[len] = '\0';
	return (pt);
}
