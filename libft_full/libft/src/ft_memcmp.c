#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	char	*pt1;
	char	*pt2;

	pt1 = (char *)s1;
	pt2 = (char *)s2;
	return (ft_strncmp(pt1, pt2, n));
}
